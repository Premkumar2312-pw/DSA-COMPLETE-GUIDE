# Getting user input
arr = list(map(int, input("Enter the elements separated by space: ").split()))

# Reversing the array
arr.reverse()

print("Array after reversal:", arr)

# Sample Output:
# Input:
# Enter the elements separated by space: 1 2 3 4 5
# Output:
# Array after reversal: [5, 4, 3, 2, 1]
